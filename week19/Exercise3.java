public class Exercise3 {
    public static void main(String[] args) {
        //for loop
        for(int i = 1; i <= 18; i++){
            System.out.println("iteration " + i + ".");
        }
        System.out.println();
        //while loop
        int count = 1;
        while(count <= 18){
            System.out.println("iteration " + count + ".");
            count++;
        }
    }
}

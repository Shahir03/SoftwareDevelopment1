public class Exercise4 {
    public static void main(String[] args) {
        //for loop
        for(int i = 1; i <= 6; i++){
            System.out.print("row " + i + ":");
            for(int j = 1; j <= 4; j++){
                System.out.print(" column " + j);
            }
            System.out.println();
        }
        System.out.println();
        
    }
}

public class Lecture{
    public static void main(String[] args) {
        int x = 1;
        while(x <= 5){
            System.out.println(x + ". While Loop");
            x++;
        }
        System.out.println();
        for(int y = 1; y <= 5; y++){
            System.out.println(y + ". For Loop");
        }
    }
}